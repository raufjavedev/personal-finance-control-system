    /**
     * Obtiene la palabra clave de recuperación de cuenta del usuario.
     *
     * @return La palabra clave de recuperación de cuenta.
     */
    public String getKeyword(){
        String keyword = txtKeyword.getText();
        if (keyword.isEmpty()) {
            Message.showWarning("La palabra clave no puede estar vacío.");
        }
        return keyword;
    }

    /**
     * Obtiene la nueva contraseña ingresada por el usuario.
     *
     * @return La nueva contraseña que el usuario ha ingresado.
     */
    public String getNewPassword(){
        char[] passwordChars = txtNewPassword.getPassword();

        if (passwordChars.length == 0) {
            Message.showWarning("La contraseña no puede estar vacía.");
        }

        String newPassword = new String(passwordChars);
        Arrays.fill(passwordChars, '\0');
        return newPassword;
    }

    public String getRepeatPassword(){
        char[] passwordChars = txtRepeatPassword.getPassword();

        if (passwordChars.length == 0) {
            Message.showWarning("Repita la contraseña.");
        }

        String repeatPassword = new String(passwordChars);
        Arrays.fill(passwordChars, '\0');
        return repeatPassword;
    }

    /**
     * Obtiene la contraseña repetida ingresada por el usuario para su confirmación.
     *
     * @return La contraseña repetida que el usuario ha ingresado.
     */
    public boolean isValidPassword(String password) {
        int minLength = 4;
        int maxLength = 8;

        if (password == null) {
            return true;
        }

        int length = password.length();
        return length < minLength || length > maxLength;
    }

    /**
     * Válida si algún campo (como nombre de usuario, contraseña o cualquier otro campo relevante) está vacío.
     *
     * @return {@code true} si algún campo está vacío, de lo contrario {@code false}.
     */
    public boolean isFieldsEmpty() {
        return getKeyword().isEmpty() || getNewPassword().isEmpty() || getRepeatPassword().isEmpty();
    }

    /**
     * Limpia todos los campos, restableciéndolos a un estado vacío.
     */
    public void clearFields(){
        txtKeyword.setText("");
        txtNewPassword.setText("");
        txtRepeatPassword.setText("");
    }
